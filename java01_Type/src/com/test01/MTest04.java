package com.test01;

public class MTest04 {
	
	//문자형 'char(2)',논리형 boolean(1)
	//문자열 "String"
	public static void main(String[] args) {
		char c01='a';
		System.out.println(c01);	//a
		char c02='b';
		int c03=c02;	//ascii code
		System.out.println(c03);	//98
		
		String s01="ab";
		System.out.println(s01);	//ab
		String s02="cedfg";
		System.out.println(s02);	//cedfg
		System.out.println(s01+s02);	//abcdefg
		
		char last_name='\uc774';
		System.out.println(last_name);	//이
		
		System.out.println("--------------");
		
		boolean bl01=true;
		boolean bl02=false;
		System.out.println(bl01);	//true
		System.out.println(bl02);	//false
		
		
		
		
		
		
		
	}

}
